<?php

namespace App\Http\Controllers;

use App\Models\Categoria;
use Illuminate\Http\Request;

class CategoriaController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $categorias = Categoria::latest()->paginate(5);

        return view('Categoria.index', compact('categorias'))
            ->with('i', (request()->input('page', 1) - 1) * 5);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('Categoria.cadastro');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nome_categoria' => 'required',
        ]);

        Categoria::create($request->all());

        return redirect()->route('categoria.index')
            ->with('success', 'Categoria cadastrada com Sucesso.');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Categoria  $categoria
     * @return \Illuminate\Http\Response
     */
    public function show(Categoria $categoria)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Categoria  $categoria
     * @return \Illuminate\Http\Response
     */
    public function edit(Categoria $categoria)
    {
        $categoria = Categoria::first();

        return view('Categoria.editar', compact('categoria'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Categoria  $categoria
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'nome_categoria' => 'required',
        ]);
       try {
        $categoria = Categoria::find($id);
        $categoria->nome_categoria = $request->nome_categoria;
        $categoria->save();
            return redirect()->route('categoria.index')
            ->with('success', 'Categoria atualizada');
       } catch (\Throwable $th) {
            return redirect()->route('categoria.index')
            ->with('error', 'Não foi possível atualizar a categoria');
       }
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Categoria  $categoria
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try {
            Categoria::destroy($id);
            return redirect('categoria')->with('success', 'Categoria excluida!');
        } catch (\Throwable $th) {
            return redirect('categoria')->with('error', 'Não foi possível excluir a categoria, ela esta sendo utilizada!');
        }

        // $res =  $categoria->delete($categoria);
        // var_dump($res);die;
        // return redirect()->route('categoria.index')
        //     ->with('success', 'Categoria excluida');
    }
}
